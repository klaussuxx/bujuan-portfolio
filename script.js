const lightbox = document.querySelector("#lightbox");
const lightboxImage = document.querySelector("#lightbox-image");
const lightboxCaption = document.querySelector("#lightbox-caption");

function renderCards(containerId, works, hasFilter = false) {
  const container = document.querySelector(containerId);
  container.innerHTML = works
    .map((work) => {
      const [title, label, filterOrSrc, maybeSrc, maybeVariant] = work;
      const filter = hasFilter ? filterOrSrc : "";
      const src = hasFilter ? maybeSrc : filterOrSrc;
      const variant = hasFilter ? maybeVariant : maybeSrc;
      const isCollapsed = variant === "collapsed";
      return `
        <figure class="work-card ${isCollapsed ? "is-collapsed" : ""}" ${hasFilter ? `data-filter="${filter}"` : ""}>
          <button type="button" data-src="${src}" data-title="${title}" data-label="${label}">
            <img src="${src}" alt="${title}" loading="lazy" decoding="async" />
          </button>
          ${isCollapsed ? '<button class="expand-control" type="button">展开长图</button>' : ""}
          <figcaption><span>${title}</span><small>${label}</small></figcaption>
        </figure>
      `;
    })
    .join("");
}

renderCards("#brand-gallery", brandWorks, true);
renderCards("#official-gallery", officialWorks, true);
renderCards("#matrix-gallery", matrixWorks, true);

function setupCardReveal() {
  document.querySelectorAll(".work-card").forEach((card) => card.classList.add("is-visible"));
}

setupCardReveal();

function applyFilter(group, filter, button) {
  const gallery = document.querySelector(`#${group}-gallery`);
  document.querySelectorAll(`[data-filter-group="${group}"]`).forEach((item) => item.classList.remove("is-active"));
  button.classList.add("is-active");
  gallery.querySelectorAll(".work-card").forEach((card) => {
      const shouldShow = filter === "all" || card.dataset.filter === filter;
      card.classList.toggle("is-hidden", !shouldShow);
      if (!shouldShow) card.classList.remove("is-visible");
  });
  window.requestAnimationFrame(() => {
    gallery.querySelectorAll(".work-card:not(.is-hidden)").forEach((card, index) => {
      card.style.transitionDelay = `${Math.min(index % 3, 2) * 24}ms`;
      card.classList.add("is-visible");
    });
    const firstVisible = gallery.querySelector(".work-card:not(.is-hidden)");
    if (firstVisible) {
      firstVisible.scrollIntoView({ behavior: "smooth", block: "start" });
    }
  });
}

document.querySelectorAll(".filter-button").forEach((button) => {
  button.addEventListener("click", () => {
    applyFilter(button.dataset.filterGroup, button.dataset.filter, button);
  });
});

document.body.addEventListener("click", (event) => {
  const expandButton = event.target.closest(".expand-control");
  if (expandButton) {
    const card = expandButton.closest(".work-card");
    const collapsed = card.classList.toggle("is-collapsed");
    expandButton.textContent = collapsed ? "展开长图" : "收起长图";
    if (collapsed) card.scrollIntoView({ behavior: "smooth", block: "center" });
    return;
  }

  const trigger = event.target.closest(".work-card button");
  if (trigger?.classList.contains("expand-control")) return;
  if (!trigger) return;
  lightboxImage.src = trigger.dataset.src;
  lightboxImage.alt = trigger.dataset.title;
  lightboxCaption.textContent = `${trigger.dataset.title} / ${trigger.dataset.label}`;
  lightbox.classList.add("is-open");
  lightbox.setAttribute("aria-hidden", "false");
});

function closeLightbox() {
  lightbox.classList.remove("is-open");
  lightbox.setAttribute("aria-hidden", "true");
  lightboxImage.src = "";
}

document.querySelector(".lightbox-close").addEventListener("click", closeLightbox);
lightbox.addEventListener("click", (event) => {
  if (event.target === lightbox) closeLightbox();
});
document.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && lightbox.classList.contains("is-open")) closeLightbox();
});
